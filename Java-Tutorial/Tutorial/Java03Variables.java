// java variables- declaration and use
    public class Java03Variables {
      public static void main(String[] args) {
        // Declare and initialize integer 'x' with the value 5.
        int x = 5;

        // Declare and initialize double 'y' with the value 10.5.
        double y = 10.5;

        // Declare and initialize character 'c' with the character 'c'.
        char c = 'c';

        // Declare and initialize boolean 'b' with the value 'true'.
        boolean b = true;

        // Declare and initialize a string 's' with the text "Hello World".
        String s = "Hello World";

        // Print the value of 'x' to the console.
        System.out.println(x);

        // Print the value of 'y' to the console.
        System.out.println(y);

        // Print the value of 'c' to the console.
        System.out.println(c);

        // Print the value of 'b' to the console.
        System.out.println(b);

        // Print the value of 's' to the console.
        System.out.println(s);

    // use print to print naming rue of varibles in java 
    System.out.println("Naming rule in Java:");
    System.out.println("1. Variable names should start with a letter or an underscore.");
    System.out.println("2. Variable names should not start with a number.");

    System.out.println("3. Variable names should be unique.");
    System.out.println("4. Variable names should not contain spaces or special character.");
    System.out.println("5. Variable names should not be keywords.");
    System.out.println("6. Variable names should be descriptive.");
    System.out.println("7. Variable names should be short.");
    System.out.println("8. Variable names should be camelCase.");

  }
}
